package currentfacerecognition;

public class Nose {

	int noseWidth;
	int noseHeight;
	
	public Nose(int w, int h)
	{
		this.noseWidth = w;
		this.noseHeight = h;
	}
	
	public int getNoseWidth()
	{
		return this.noseWidth;
	}
	
	public int getNoseHeight()
	{
		return this.noseHeight;
	}
	
}
