package currentfacerecognition;

public class Forehead {

	int foreheadHeight;
	int foreheadWidth;
	
	public Forehead(int h, int w)
	{
		foreheadHeight = h;
		foreheadWidth = w;
	}
	
	public int getForeheadHeight()
	{
		return this.foreheadHeight;
	}
	
	public int getForeheadWidth()
	{
		return this.foreheadWidth;
	}
}
